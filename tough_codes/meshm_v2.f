C-----THIS IS THE EXECUTIVE ROUTINE FOR THE *MESHMAKER* MODULE.
C
C     IT IS CALLED WHEN DATA BLOCK "MESHM" IS PRESENT IN THE INPUT
C     FILE, AND WILL THEN PROCEED TO PERFORM INTERNAL MESH GENERATION.
C
      implicit double precision (a-h,o-z)
      implicit integer*8 (i-n)
c
      COMMON/MMC/MM,MC
      common/ff/h1
