  114 FORMAT(' FILE *MINC* EXISTS --- OPEN AS AN OLD FILE')
      OPEN(10,FILE='MINC',STATUS='OLD')
C
  120 CONTINUE
C
      CALL MINC
      GOTO 100
C
  101 CONTINUE
      WRITE (34, 102)
  102 FORMAT(/' ',131('*')//' MESH GENERATION COMPLETE --- EXIT FROM ',
     X'MODULE *MESHMAKER*'/)
      RETURN
      END
      BLOCK DATA MM
