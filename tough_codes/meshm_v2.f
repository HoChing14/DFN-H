     x' ',131('*')/' *',18X,'MESHMAKER - MINC: GENERATE MULTIPLE'
     X,' INTERACTING CONTINUA MESH FOR FRACTURED MEDIUM',29X,'*'/
     X' ',131('*')/)
C
      INQUIRE(FILE='MINC',EXIST=EX)
      IF(EX) GOTO 112
      WRITE (34, 113)
  113 FORMAT(' FILE *MINC* DOES NOT EXIST --- OPEN AS A NEW FILE')
      OPEN(10,FILE='MINC',STATUS='NEW')
      GOTO 120
C
